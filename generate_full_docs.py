def _sitemap_entry(loc: str, priority: str, changefreq: str) -> str:
    import datetime
    today = datetime.date.today().isoformat()
    return (
        f"  <url>\n"
        f"    <loc>{loc}</loc>\n"
        f"    <lastmod>{today}</lastmod>\n"
        f"    <changefreq>{changefreq}</changefreq>\n"
        f"    <priority>{priority}</priority>\n"
        f"  </url>"
    )


